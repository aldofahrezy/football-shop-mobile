class MyHomePage extends StatelessWidget {
  MyHomePage({super.key});

  final String nama = "Muhammad Aldo Fahrezy"; //nama
  final String npm = "2406423055"; //npm
  final String kelas = "C"; //kelas

  final List<ItemHomepage> items = [
    ItemHomepage("All Products", Icons.list, Colors.blueGrey.shade700),
    ItemHomepage("Add Product", Icons.add, Colors.black),
    ItemHomepage("Logout", Icons.logout, Colors.red.shade700),
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('The Kickoff Zone')),
      drawer: const LeftDrawer(),
      body: Padding(
        padding: const EdgeInsets.all(16.0),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.center,
          children: [
            Row(
              mainAxisAlignment: MainAxisAlignment.spaceEvenly,
              children: [
